// Matches Seats.aero API response shape so swapping to real API is straightforward
export type AwardAvailability = {
  id: string
  origin: string
  destination: string
  date: string // YYYY-MM-DD
  // Cabin availability flags
  economyAvailable: boolean
  premiumEconomyAvailable: boolean
  businessAvailable: boolean
  firstAvailable: boolean
  // Mileage costs per cabin (null if not available)
  economyMiles: number | null
  premiumEconomyMiles: number | null
  businessMiles: number | null
  firstMiles: number | null
  // Remaining seats (0 if unavailable)
  businessRemainingSeats: number
  firstRemainingSeats: number
  // Taxes and fees in USD
  economyTaxes: number | null
  businessTaxes: number | null
  firstTaxes: number | null
  // Which award program has this availability
  source: string // matches AwardProgram.id (e.g., 'united', 'british-airways')
  // Which airline operates the flight
  operatingAirline: string
  // Whether this is a direct flight
  direct: boolean
  // Approximate retail price in USD for comparison
  retailPrice: number | null
}

// Helper to generate unique IDs
let idCounter = 0
function nextId(): string {
  idCounter++
  return `mock-${idCounter}`
}

